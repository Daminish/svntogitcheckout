package com.capco.entity;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "temp_emp_info")
public class Employee {

	@Id
	@Column(name = "Emp_Id")
	private int empid;
	
	@Column(name="Emp_Name")
	private String empname;

	@Column(name="mlevel")
	private String mlevel;
	
	@Column(name="Gender")
	private String gender;

	@Column(name="Designation")
	private String designation;
	
	@Column(name="Official_Email_Id")
	private String mailid;
	
	@Column(name="Capco_Login_Id")
	private String capcoLoginID;

	public int getEmpid() {
		return empid;
	}

	public void setEmpid(int empid) {
		this.empid = empid;
	}

	public String getEmpname() {
		return empname;
	}

	public void setEmpname(String empname) {
		this.empname = empname;
	}

	public String getMlevel() {
		return mlevel;
	}

	public void setMlevel(String mlevel) {
		this.mlevel = mlevel;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getDesignation() {
		return designation;
	}

	public void setDesignation(String designation) {
		this.designation = designation;
	}

	public String getMailid() {
		return mailid;
	}

	public void setMailid(String mailid) {
		this.mailid = mailid;
	}

	public String getCapcoLoginID() {
		return capcoLoginID;
	}

	public void setCapcoLoginID(String capcoLoginID) {
		this.capcoLoginID = capcoLoginID;
	}
}
