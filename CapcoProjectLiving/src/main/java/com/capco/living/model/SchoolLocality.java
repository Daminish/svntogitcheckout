package com.capco.living.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author e5544847,e5544698
 */
 
@Entity
@Table(name = "school_locality", schema = "capco_living_portal")
public class SchoolLocality {
	
	private static final long serialVersionUID = 1L;
	
    @Id
	@Column(name = "school_id")
	private int school_id;
    @Column(name = "locality_id")
    private int locality_id;
    @Column(name = "school_rating")
    private String school_rating;
	public int getSchool_id() {
		return school_id;
	}
	public void setSchool_id(int school_id) {
		this.school_id = school_id;
	}
	public int getLocality_id() {
		return locality_id;
	}
	public void setLocality_id(int locality_id) {
		this.locality_id = locality_id;
	}
	public String getSchool_rating() {
		return school_rating;
	}
	public void setSchool_rating(String school_rating) {
		this.school_rating = school_rating;
	}
    
    

}
